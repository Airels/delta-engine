package fr.r1r0r0.deltaengine.model;

public final class Dimension {

    private final double width, height;

    public Dimension(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public double getHeight() {
        return height;
    }
}
