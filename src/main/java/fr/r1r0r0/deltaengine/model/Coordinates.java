package fr.r1r0r0.deltaengine.model;

public class Coordinates {
    private double x, y;
    
    public Coordinates(double x, double y) {
        this.x = x;
        this.y = y;
    }
    
    public double getX() {
        return x;
    }
    
    public double getY() {
        return y;
    }
}
